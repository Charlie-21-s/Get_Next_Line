int	ft_strequ(char const *s1, char const *s2)
{
	long int	i;

	if (!s1 || !s2)
		return (0);
	i = -1;
	while (s1[++i])
		if (s1[i] != s2[i])
			return (0);
	if (s1[i] != s2[i])
		return (0);
	return (1);
}
